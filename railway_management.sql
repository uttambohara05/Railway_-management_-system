CREATE TABLE PAYMENT (
    PAY_ID    INT PRIMARY KEY,         
    PAY_DATE  NVARCHAR2(50),
    PAY_DESC  NVARCHAR2(50),
    PAID_AMT  NVARCHAR2(50),
    TICKET_ID INT,
    USER_ID       INT,
    CONSTRAINT fk_payment_ticket FOREIGN KEY (TICKET_ID) REFERENCES TICKET(TICKET_ID),
    CONSTRAINT fk_payment_user   FOREIGN KEY (USER_ID)        REFERENCES USERS(USER_ID)
);



CREATE TABLE BOOKS_AND_CANCEL (
    BOOKING_ID   INT PRIMARY KEY,
    USER_ID      INT,
    FIRST_NAME   NVARCHAR2(50),
    BOOKING_DATE NVARCHAR2(50),
    STATUS       NVARCHAR2(20)
        CHECK (STATUS IN ('BOOKED', 'CANCELLED')),
    CONSTRAINT fk_books_user FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID)
);


CREATE TABLE TRAVELS_IN (
    USER_ID    INT,
    TRAIN_NO INT,
    CONSTRAINT fk_travels_user  FOREIGN KEY (USER_ID)       REFERENCES USERS( USER_ID),
    CONSTRAINT fk_travels_train FOREIGN KEY (TRAIN_NO) REFERENCES TRAIN(TRAIN_NO)
);


CREATE TABLE REACHES (
    TRAIN_NO     INT,
    ARRIVAL_TIME NVARCHAR2(50),
    CONSTRAINT fk_reaches_train FOREIGN KEY (TRAIN_NO) REFERENCES TRAIN(TRAIN_NO)
);


CREATE TABLE START_TABLE (
    TRAIN_NO     INT,
    ARRIVAL_TIME VARCHAR2(50),
    CONSTRAINT fk_start_train FOREIGN KEY (TRAIN_NO) REFERENCES TRAIN(TRAIN_NO)
);



