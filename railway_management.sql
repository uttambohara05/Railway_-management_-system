

CREATE TABLE USERS (
    USER_ID INT PRIMARY KEY,
    GENDER      NVARCHAR2(10),
    CITY        NVARCHAR2(50),
    PIN_CODE    NVARCHAR2(20),
    STATE       NVARCHAR2(50),
    AGE         NUMBER,
    FIRST_NAME  NVARCHAR2(50),
    MIDDLE_NAME NVARCHAR2(50),
    LAST_NAME   NVARCHAR2(50)
);
DESCRIBE USERS;



CREATE TABLE TRAIN (
    TRAIN_NO     INT PRIMARY KEY,
    CAPACITY     INT,
    TRAIN_NAME   NVARCHAR2(50),
    TRAIN_DESC   NVARCHAR2(50),
    DESTINATION  NVARCHAR2(50),
    ARRIVAL_TIME NVARCHAR2(50)
);




