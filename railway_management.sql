CREATE TABLE STATION (
    STATION_NO   NUMBER PRIMARY KEY,
    ARRIVAL_TIME VARCHAR2(50),
    HAULT        VARCHAR2(50),
    DEPARTURE    VARCHAR2(50),
    NAME         VARCHAR2(50),
    TRAIN_NO     NUMBER,
    CONSTRAINT fk_station_train FOREIGN KEY (TRAIN_NO) REFERENCES TRAIN(TRAIN_NO)
);



CREATE TABLE TICKET (
    TICKET_ID       INT PRIMARY KEY,
    NO_OF_PASSENGER NVARCHAR2(20),
    FIRST_NAME      NVARCHAR2(50),
    TICKET_PRICE    NVARCHAR2(20),          
    PNR_NO          NVARCHAR2(20),
    TRAIN_NO        INT,
    USER_ID          INT,
    STATUS          NVARCHAR2(20)           
        CHECK (STATUS IN ('CONFIRMED', 'RAC', 'WAITING')),
    CONSTRAINT fk_ticket_train FOREIGN KEY (TRAIN_NO) REFERENCES TRAIN(TRAIN_NO),
    CONSTRAINT fk_ticket_user  FOREIGN KEY (USER_ID)  REFERENCES USERS(USER_ID)
);

